package atividade;

public class Cadastro {
	
	protected String nome;
	protected int cpf;
	protected int telefone;
	protected String endereco;
	
	public Cadastro(String nome, int cpf, int telefone, String endereco) {
		
		this.nome = nome;
		this.cpf = cpf;	
		this.telefone = telefone;
		this.endereco = endereco;
	}

}
